package vo;

public class TicketVo {
	 private String ticket_no;
	 private String tkt_name;
	 private String tkt_lgu;
	 private int tkt_price;
	 private String employee_no;
	 private int tkt_time;
	 
	public String getTicket_no() {
		return ticket_no;
	}
	public void setTicket_no(String ticket_no) {
		this.ticket_no = ticket_no;
	}
	public String getTkt_name() {
		return tkt_name;
	}
	public void setTkt_name(String tkt_name) {
		this.tkt_name = tkt_name;
	}
	public String getTkt_lgu() {
		return tkt_lgu;
	}
	public void setTkt_lgu(String tkt_lgu) {
		this.tkt_lgu = tkt_lgu;
	}
	public int getTkt_price() {
		return tkt_price;
	}
	public void setTkt_price(int tkt_price) {
		this.tkt_price = tkt_price;
	}
	public String getEmployee_no() {
		return employee_no;
	}
	public void setEmployee_no(String employee_no) {
		this.employee_no = employee_no;
	}
	public int getTkt_time() {
		return tkt_time;
	}
	public void setTkt_time(int tkt_time) {
		this.tkt_time = tkt_time;
	}
	 
}