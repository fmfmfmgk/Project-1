package vo;

public class FeedBackVo {
	
	 private String feedback_no;
	 private String feedback_til;
	 private String feedback_con;
	 private String feedback_date;
	 private String feedback_yn;
	 private String users_no;
	 
	public String getFeedback_no() {
		return feedback_no;
	}
	public void setFeedback_no(String feedback_no) {
		this.feedback_no = feedback_no;
	}
	public String getFeedback_til() {
		return feedback_til;
	}
	public void setFeedback_til(String feedback_til) {
		this.feedback_til = feedback_til;
	}
	public String getFeedback_con() {
		return feedback_con;
	}
	public void setFeedback_con(String feedback_con) {
		this.feedback_con = feedback_con;
	}
	public String getFeedback_date() {
		return feedback_date;
	}
	public void setFeedback_date(String feedback_date) {
		this.feedback_date = feedback_date;
	}
	public String getFeedback_yn() {
		return feedback_yn;
	}
	public void setFeedback_yn(String feedback_yn) {
		this.feedback_yn = feedback_yn;
	}
	public String getUsers_no() {
		return users_no;
	}
	public void setUsers_no(String users_no) {
		this.users_no = users_no;
	}
	 
}
